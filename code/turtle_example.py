import turtle

def draw_square():
    window = turtle.Screen()
    window.bgcolor("red")

    tom = turtle.Turtle()
    tom.forward(200)
    tom.right(200)
    tom.forward(200)
    tom.right(200)
    tom.forward(200)
    tom.right(200) 
    
    window.exitonclick()    


draw_square() 
   
    

    
 
    
